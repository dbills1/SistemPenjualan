
package Model;

public class Mekanik {
    private Integer kdmekanik;
    private String nmmekanik;
    private String alamatmekanik;
    private String notelpmekanik;

    public Integer getKdmekanik() {
        return kdmekanik;
    }

    public void setKdmekanik(Integer kdmekanik) {
        this.kdmekanik = kdmekanik;
    }

    public String getNmmekanik() {
        return nmmekanik;
    }

    public void setNmmekanik(String nmmekanik) {
        this.nmmekanik = nmmekanik;
    }

    public String getAlamatmekanik() {
        return alamatmekanik;
    }

    public void setAlamatmekanik(String alamatmekanik) {
        this.alamatmekanik = alamatmekanik;
    }

    public String getNotelpmekanik() {
        return notelpmekanik;
    }

    public void setNotelpmekanik(String notelpmekanik) {
        this.notelpmekanik = notelpmekanik;
    }
    
    
}
