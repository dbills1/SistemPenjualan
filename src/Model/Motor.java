package Model;

public class Motor {
    private Integer kdmotor;
    private String platmotor;
    private String warna;
    private String merk;
    private String tahun;
    private String notelp;
    private String alamat;
    private String namapemilik;

    public Integer getKdmotor() {
        return kdmotor;
    }

    public void setKdmotor(Integer kdmotor) {
        this.kdmotor = kdmotor;
    }

    public String getPlatmotor() {
        return platmotor;
    }

    public void setPlatmotor(String platmotor) {
        this.platmotor = platmotor;
    }

    public String getWarna() {
        return warna;
    }

    public void setWarna(String warna) {
        this.warna = warna;
    }

    public String getMerk() {
        return merk;
    }

    public void setMerk(String merk) {
        this.merk = merk;
    }

    public String getTahun() {
        return tahun;
    }

    public void setTahun(String tahun) {
        this.tahun = tahun;
    }

    public String getNotelp() {
        return notelp;
    }

    public void setNotelp(String notelp) {
        this.notelp = notelp;
    }

    public String getAlamat() {
        return alamat;
    }

    public void setAlamat(String alamat) {
        this.alamat = alamat;
    }

    public String getNamapemilik() {
        return namapemilik;
    }

    public void setNamapemilik(String namapemilik) {
        this.namapemilik = namapemilik;
    }

    
}
